def mismos_caracteres(lista):
    resultado = []
    for palabra in lista:
        caracteres_unicos = set(palabra)
        if len(caracteres_unicos) == len(palabra):
            resultado.append(palabra)
    return resultado

lista_palabras = ["amor", "roma", "perro", "murci√©lago"]
resultado = mismos_caracteres(lista_palabras)
print("Elementos con los mismos caracteres:", resultado)